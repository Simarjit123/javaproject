
public class Program {
	public static void main(String[] args) {
		// declaring variables
		int num1 = 30, num2 = 20, addition, subtraction, multiply;
		// Adding two numbers
		addition = num1 + num2;
		System.out.println("Result of adding two numbers is:" + addition);
		// subtracting two numbers
		subtraction = num1 - num2;
		System.out.println("Result of subtracting two numbers is:" + subtraction);
		// multiplying two numbers
		multiply = num1 * num2;
		System.out.println("Result of multiplying two numbers is:" + multiply);
	}
}
